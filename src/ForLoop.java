public class ForLoop {
    public static void main(String[] args) throws Exception {
        // For Loop
        for (int i = 0; i < 5; i++) {
            System.out.println("Perulangan ke-" + i);
        }

        // Contoh 2
        for (int i = 1; i <= 10; i++) {
            System.out.println("Perulangan ke-" + i);
        }

        // Himpunan = {1, 2, 3, 4, 5}
        
    }
}
